package Matematica01;

public class VolumeCilindro {

    public static void main(String[] args) {

        double raio = 4;
        double altura = 10;

        // Lógica que calcula o volume
        double volume = Math.PI * raio * raio * altura;

        System.out.println("O volume do cilindro é: " + volume);
    }
}
